// A binary tree is a data structure in which each node has at most two children.
//  It is a hierarchical structure consisting of nodes, where each node can have:

// Left child: A node that is the left child of the parent.
// Right child: A node that is the right child of the parent.
// Key Concepts
// Root: The topmost node of the tree.
// Node: Each element in the binary tree containing data and links to its children.
// Children: Nodes that extend from a parent node. A binary tree node can have up to two children.
// Parent: A node that has children.
// Leaf: A node that has no children (both left and right children are null).
// Subtree: A tree that is a child of another node.
// Properties of a Binary Tree
// Depth: The depth of a node is the number of edges from the root to the node.
// Height: The height of a node is the number of edges from the node to the deepest leaf.
// Full Binary Tree: Every node has either 0 or 2 children.
// Perfect Binary Tree: A binary tree where all internal nodes have two children and all leaves are at the same level.
// Complete Binary Tree: All levels are completely filled except possibly for the last level, which is filled from left to right.
